function getNestedI18nValue(lang, keyPath) {
    if (!i18n || !i18n[lang]) {
        return undefined;
    }
    
    const keys = keyPath.split('.');
    let value = i18n[lang];
    
    for (const k of keys) {
        if (value && value[k] !== undefined) {
            value = value[k];
        } else {
            return undefined;
        }
    }
    
    return value;
}

